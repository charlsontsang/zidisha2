		<p>Are you passionate about connecting people, helping communities grow, and making an impact around the world? We give volunteers and interns the unique opportunity to gain hands-on experience in microfinance and international development, while extending life-changing business-growth opportunities to entrepreneurs in some of the world's most disadvantaged locations.</p>
		 
		<h3>Virtual Volunteering Opportunities</h3>
		
		<p>If you enjoy volunteering on a flexible schedule from the comfort of your own home, listen up: We are looking for volunteers who would like to contribute two or more hours per week to join our Virtual Volunteer team.</p>

		<p>Virtual Volunteers work with borrowers to help them improve the presentation of their loan profiles, help welcome new lenders, and assist in translation of borrower content. This is a position that may be undertaken on a flexible schedule, from any location in the world with an internet connection.</p>	

		<p>If you would like to learn more about joining our Virtual Volunteer team, please send an email to <a href="mailto:service@zidisha.org">service@zidisha.org</a>.</p>
		 
		<h3>Microfinance Internships</h3>
		
		<p>We are currently offering full-time Country Ambassador internships in the countries in which we have lending programs. Country Ambassadors spend their time meeting with Zidisha entrepreneurs at their business locations and homes — and helping share their stories with the Zidisha community.</p>

		<p>We also have openings for part-time Country Liaison Interns, Community Managers, and University Outreach Organizers. These interns can be based anywhere in the world, with flexible work hours to accommodate class or work schedules.</p>	

		<p>Finally, we welcome entrepreneurial individuals with expertise in a specific developing country to join us as New Country Expansion Coordinators. This is a full-time or part-time opportunity for leaders who can assist us to launch new Zidisha lending services in your chosen country.</p>	
		 
